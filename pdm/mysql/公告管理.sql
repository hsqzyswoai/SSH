    I_STAFF_ID  DECIMAL(15,0) NOT NULL,
    S_DEPT_NAME   VARCHAR(128)  NOT NULL,
    I_DEPT_ID  DECIMAL(15,0) NOT NULL,
    S_DOMAN_NAME  VARCHAR(128)  NOT NULL,
    I_DOMAN_ID   DECIMAL(15,0)  NOT NULL,
    S_STAFF_TEL  VARCHAR(30)   NOT NULL,
    D_CREATE_DATE  TIMESTAMP  NOT NULL,
    I_WFINST_ID  DECIMAL(15,0)  NOT NULL,
    I_ACTINST_ID  DECIMAL(15,0)  NOT NULL,
    I_WORKITEM_ID  DECIMAL(15,0)  NOT NULL,
    S_REMARK   VARCHAR(255)  NOT NULL,
    PRIMARY KEY (I_AUDIT_SEG_ID)
)ENGINE=INNODB DEFAULT CHARSET=utf8;


