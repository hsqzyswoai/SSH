/*公告审核表*/
CREATE TABLE T_PRO_BULLETIN_AUDIT
(
    I_AUDIT_SEG_ID  DECIMAL(15,0)  NOT NULL,
    I_BULLETIN_SEG_ID  DECIMAL(15,0)  NOT NULL,
    I_IS_MIND  DECIMAL(7,0)  NOT NULL,
