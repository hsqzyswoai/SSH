    I_TICKET_STATUS  DECIMAL(7,0)  DEFAULT 1  NOT NULL,
    I_BULLETIN_STATUS  DECIMAL(7,0)  NULL,
    I_KIND  DECIMAL(7,0)  NULL,
    I_RELATE_ID  DECIMAL(15,0) NULL,
