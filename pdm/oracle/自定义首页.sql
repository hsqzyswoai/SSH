/*PORTAL版式表*/
CREATE TABLE T_SYS_PORTAL
(
    ILAYOUT_ID    NUMBER(15,0) NULL,
    SLAYOUT_TITLE VARCHAR2(50)   NULL,
    ITYPE         NUMBER(15,0) NULL,
    ILEFTWIDTH    VARCHAR2(10)   NULL,
    ICENTERWIDTH  VARCHAR2(10)   NULL,
    IRIGHTWIDTH   VARCHAR2(10)   NULL
);


/*PORTAL内容信息表*/
CREATE TABLE T_SYS_PORTAL_CONTENT
(
    ICONTENT_ID NUMBER(15,0) NULL,
    STITLE      VARCHAR2(50)   NULL,
    SURL        VARCHAR2(200)  NULL,
    ISORT       NUMBER(15,0) NULL,
    ISVALID     NUMBER(15,0) DEFAULT 0         NULL,
    ICREATOR    NUMBER(15,0) NULL,
    DUPDATE     VARCHAR2(50)   NULL,
    CONTENTTYPE NUMBER(15,0) NULL
);


/*PORTAL定制信息表*/
CREATE TABLE T_SYS_PORTAL_ITEM
(
    IPORTAL_ID  NUMBER(15,0) NULL,
    STITLE      VARCHAR2(50)   NULL,
    ICONTENT_ID NUMBER(15,0) NULL,
    ISCOLLAPSE  NUMBER(15,0) DEFAULT 0         NULL,
    ISCLOSE     NUMBER(15,0) DEFAULT 0         NULL,
    ISVALID     NUMBER(15,0) DEFAULT 0         NULL,
    ISORT       NUMBER(15,0) NULL,
    IROW        NUMBER(15,0) NULL,
    ICOL        NUMBER(15,0) NULL,
    IUSER_ID    NUMBER(15,0) NULL,
    DUPDATE     VARCHAR2(50) NULL
);


/*版式和用户关系表*/
CREATE TABLE T_SYS_PORTL_LAYOUT_USER
(
    ILAYOUT_ID NUMBER(15,0) NULL,
    IUSER_ID   NUMBER(15,0) NULL
);


/*索引*/
CREATE INDEX idx_iLayoutId ON T_SYS_PORTAL (ILAYOUT_ID);
CREATE INDEX idx_iType ON T_SYS_PORTAL (ITYPE);

CREATE INDEX idx_iContentId ON T_SYS_PORTAL_CONTENT (ICONTENT_ID);

CREATE INDEX idx_iPortalId ON T_SYS_PORTAL_ITEM (IPORTAL_ID);
CREATE INDEX idx_iUserId ON T_SYS_PORTAL_ITEM (IUSER_ID);


